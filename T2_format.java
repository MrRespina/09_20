package Practice;

public class T2_format {

	public static void main(String[] args) {
		
		System.out.printf("%d\n", 10);
		System.out.printf("%3s\n", 10);
		System.out.printf("%04d\n", 1);
		System.out.printf("%.4f\n", 1.98766);
		
		System.out.printf("%s", "오늘은 09월이고, 내 시력은 1.34다\n");
		
		System.out.printf("오늘은 %02d월이고, 내 시력은 %.2f다.",9,1.339);

	}

}
